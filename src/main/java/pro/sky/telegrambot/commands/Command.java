package pro.sky.telegrambot.commands;

import pro.sky.telegrambot.context.MessageContext;
/**
 * Интерфейс для команд, обрабатываемых Telegram ботом.
 *
 * <p>Этот интерфейс определяет стандартный контракт для всех команд,
 * которые могут быть выполнены ботом. Каждая команда должна реализовывать
 * методы для выполнения своей логики и получения имени команды.
 */
public interface Command {
    /**
     * Выполняет команду на основе предоставленного контекста сообщения.
     *
     * <p>Метод выполняет основную логику команды, принимая объект
     * {@link MessageContext}, который содержит информацию о текущем
     * сообщении и его контексте.
     *
     * @param messageContext контекст сообщения, содержащий параметры команды
     */
    void execute(MessageContext messageContext);
    /**
     * Возвращает имя команды.
     *
     * <p>Этот метод используется для идентификации команды среди
     * других команд. Имя команды обычно совпадает с ее функциональностью.
     *
     * @return имя команды как строка
     */
    String getNameCommand();
}
